def ds_length(string):
    return len(string)


def ds_concat(string1, string2):
    return string1 + string2


def ds_compare(string1, string2):
    return string1 == string2


def ds_upper(string):
    return string.upper()


def ds_replace(fullstring, oldstring, newstring):
    return fullstring.replace(oldstring, newstring)
